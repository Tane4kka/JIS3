package by.bariloegor.mathOperations;

public class MathOperationsDemo {
    public static void main(String[] args){
        int x = 16;
        int y = 4;
        System.out.println("Math operations on " + x + " and " + y + " : ");
        int addition = x + y;
        System.out.println("Addition: " + x + " + " + y + " = " + addition);
        int subtraction = x - y;
        System.out.println("Substraction: " + x + " - " + y + " = " + subtraction);
        int division = x / y;
        System.out.println("Division: " + x + " / " + y + " = " + division);
        int multiplication = x * y;
        System.out.println("Multiplication: " + x + " * " + y + " = " + multiplication);
    }
}
